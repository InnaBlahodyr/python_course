# Ціна може бути вказана з копійками int -> float
# Можна ввести від"ємну ціну, на це бажано зробити перевірку
price = int(input("Введіть ціну за кг "))

i = 0
# чудове рішення винести розрахунок ціни за шматок за межі циклу
partPrice = price/1000
for step in range(50, 1000, 50):
    # Змінну i можна не використовувати, просто виводити ціну та вагу
    # Однак, це чисто про всяк випадок
    i = i + 1
    b = step * partPrice
    print(i, "Вага :", step, "Ціна :", b)